// Meses del año

// Crear un programa que pida al usuario un numero y mostrar en un mensaje el nombre del mes correspondiente.

const mes = Number(prompt("Ingrese un número del 1 al 12"));


switch(mes) {
    case 1:
        alert("El mes elegido es enero.");
    break

    case 2:
        alert("El mes elegido es febrero.");
    break

    case 3:
        alert("El mes elegido es marzo.");
    break

    case 4:
        alert("El mes elegido es abril.");
    break

    case 5:
        alert("El mes elegido es mayo.");
    break

    case 6:
        alert("El mes elegido es junio.");
    break

    case 7:
        alert("El mes elegido es julio.");
    break

    case 8:
        alert("El mes elegido es agosto.");
    break

    case 9:
        alert("El mes elegido es septiembre.");
    break

    case 10:
        alert("El mes elegido es octubre.");
    break

    case 11:
        alert("El mes elegido es noviembre.");
    break

    case 12:
        alert("El mes elegido es diciembre.");
    break

    default:
        alert("Elija un número correcto.");
    break
    
    

    


}